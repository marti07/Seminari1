package edu.eetac.upc.dxat.mgasull.seminari1;

public class calc {

	public int suma(int x, int y )
	{
		return x + y;
	}
	
	public int resta(int x, int y )
	{
		return x - y;
	}
	
	public int product(int x, int y )
	{
		return x * y;
	}
		
	public int div(int x, int y )
	{
		return x / y;
	}
	
	public boolean ishigher(int x, int y)
	{
		if (x > y)return true;
		else return false;
	}
}


